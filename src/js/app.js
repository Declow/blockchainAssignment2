App = {
  web3Provider: null,
  contracts: {},
  account: '0x0',

  init: function() {
    return App.initWeb3();
  },

  initWeb3: function() {
    if (typeof web3 !== 'undefined') {
      // If a web3 instance is already provided by Meta Mask.
      App.web3Provider = web3.currentProvider;
      web3 = new Web3(web3.currentProvider);
    } else {
      // Specify default instance if no web3 instance provided
      App.web3Provider = new Web3.providers.HttpProvider('http://localhost:8545');
      web3 = new Web3(App.web3Provider);
    }
    return App.initContract();
  },

  initContract: function() {
    $.getJSON("DataManager.json", function(DataManager) {
      // Instantiate a new truffle contract from the artifact
      App.contracts.DataManager = TruffleContract(DataManager);
      // Connect provider to interact with contract
      App.contracts.DataManager.setProvider(App.web3Provider);

      return App.render();
    });
  },

  deposit: function() {
    var amount = $('#amount').val();
    var id = $('#accId').val();
    App.contracts.DataManager.deployed().then(function(instance) {
      return instance.deposit(amount, id);
    }).then(function(result) {
      // Wait for votes to update
      $("#content").hide();
      $("#loader").show();
    }).catch(function(err) {
      console.error(err);
    });
  },

  withdraw: function() {
    var amount = $('#amountW').val();
    var id = $('#accIdW').val();
    App.contracts.DataManager.deployed().then(function(instance) {
      return instance.withdraw(amount, id);
    }).then(function(result) {
      // Wait for votes to update
      $("#content").hide();
      $("#loader").show();
    }).catch(function(err) {
      console.error(err);
    });
  },

  addAccount: function() {
    var account = $('#accName').val();
    App.contracts.DataManager.deployed().then(function(instance) {
      return instance.addAccount(account);
    }).then(function(result) {
      // Wait for votes to update
      $("#content").hide();
      $("#loader").show();
    }).catch(function(err) {
      console.error(err);
    });
  },

  render: function() {
    var dataInstance;
    var loader = $("#loader");
    var content = $("#content");

    loader.show();
    content.hide();

    // Load account data
    web3.eth.getCoinbase(function(err, account) {
      if (err === null) {
        App.account = account;
        $("#accountAddress").html("Your Account: " + account);
      }
    });

    // Load contract data
    App.contracts.DataManager.deployed().then(function(instance) {
      dataInstance = instance;
      return dataInstance.accountsCount();
    }).then(function(candidatesCount) {
      var candidatesResults = $("#candidatesResults");
      candidatesResults.empty();

      for (var i = 1; i <= candidatesCount; i++) {
        dataInstance.accounts(i).then(function(candidate) {
          var id = candidate[0];
          var name = candidate[1];
          var voteCount = candidate[2];

          // Render candidate Result
          var candidateTemplate = "<tr><th>" + id + "</th><td>" + name + "</td><td>" + voteCount + "</td></tr>"
          candidatesResults.append(candidateTemplate);
        });
      }

      loader.hide();
      content.show();
    }).catch(function(error) {
      console.warn(error);
    });
  }
};

$(function() {
  $(window).load(function() {
    App.init();
  });
});